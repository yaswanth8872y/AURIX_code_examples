#include "Ifx_Types.h"
#include "Ifx_Cfg.h"
#include "IfxCpu.h"
#include "IfxWtu.h"
#include "Qspi.h"

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
QspiCommunication g_qspi;

/*********************************************************************************************************************/
/*-------------------------------------------------------Main--------------------------------------------------------*/
/*********************************************************************************************************************/
void core0_main(void)
{
    IfxCpu_enableInterrupts();
    
    /* !!WATCHDOG0 AND SAFETY WATCHDOG ARE DISABLED HERE!!
     * Enable the watchdogs and service them periodically if it is required
     */
    IfxWtu_disableCpuWatchdog(IfxWtu_getCpuWatchdogPassword());
    IfxWtu_disableSystemWatchdog(IfxWtu_getSystemWatchdogPassword());
    
    /* Initialize QSPI */
    Qspi_initQspi(&g_qspi);

    /* Activate TLE9180D enable pin */
    IfxTLE9180_activateEnable(&g_qspi.driverTle9180);

    /* Send startup configuration to TLE9180D */
    IfxTLE9180_loadStartupConfiguration(&g_qspi.driverTle9180);

    /* Deactivate TLE9180D enable pin */
    IfxTLE9180_deactivateEnable(&g_qspi.driverTle9180);

    while(1)
    {
    }
}
