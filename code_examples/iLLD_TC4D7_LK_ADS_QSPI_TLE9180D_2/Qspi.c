void Qspi_initQspi(QspiCommunication* const qspiCommunication)
{
    {
        /* create SPI0 module config */
        static IfxQspi_SpiMaster_Config        spiConfig;                       /* Define the Master Configuration          */
        IfxQspi_SpiMaster_initModuleConfig(&spiConfig, &SPI_3_MODULE);

        /* pin configuration */
        const IfxQspi_SpiMaster_Pins spiPins =
        {
                &SPI_3_CLOCK_PIN,  IfxPort_OutputMode_pushPull,          /* SCLK Pin                          (CLK)  */
                &SPI_3_MOSI_PIN,  IfxPort_OutputMode_pushPull,           /* Master Transmit Slave Receive Pin (MOSI) */
                &SPI_3_MISO_PIN,  IfxPort_InputMode_pullDown,            /* Master Receive Slave Transmit Pin (MISO) */
                IfxPort_PadDriver_cmosAutomotiveSpeed3                   /* Pad driver mode                          */
        };
        spiConfig.pins = &spiPins;                                       /* Assign Master Pins                       */
        /* set the maximum baudrate */
        spiConfig.maximumBaudrate = 50.0e6;

        /* ISR priorities and interrupt target */
        spiConfig.txPriority    = INTERRUPT_PRIORITY_QSPI3_TX;
        spiConfig.rxPriority    = INTERRUPT_PRIORITY_QSPI3_RX;
        spiConfig.erPriority    = INTERRUPT_PRIORITY_QSPI3_ERR;
        spiConfig.isrProvider   = SPI_3_HOST_CPU;
        spiConfig.vmId          = IfxSrc_VmId_0;
#if (SPI_3_USE_DMA == TRUE)
        spiConfig.dma.txDmaChannelId = SPI_3_TX_DMA_CH;
        spiConfig.dma.rxDmaChannelId = SPI_3_RX_DMA_CH;
        spiConfig.dma.useDma         = TRUE;
#endif
        /* initialize module */
        IfxQspi_SpiMaster_initModule(&qspiCommunication->spiMasterQspi3tle9180, &spiConfig);
    }

    {
        static IfxTLE9180_Config tleConfig;

        /* Configure SPI Channels for Motor Control Module */
        /* Configuration for TLE9180 communication         */
        /* create channel config */
        static IfxQspi_SpiMaster_ChannelConfig spiChConfig;
        IfxQspi_SpiMaster_initChannelConfig(&spiChConfig, &qspiCommunication->spiMasterQspi3tle9180);

        /* set the baudrate for this channel */
        spiChConfig.ch.baudrate = IFX_TLE9180_BAUDRATE;

        /* set the transfer data width */
        spiChConfig.ch.mode.dataWidth = IFX_TLE9180_DATAWIDTH;

        spiChConfig.ch.mode.csTrailDelay      = SpiIf_SlsoTiming_1;
        spiChConfig.ch.mode.csInactiveDelay   = SpiIf_SlsoTiming_7;
        spiChConfig.ch.mode.shiftClock        = SpiIf_ShiftClock_shiftTransmitDataOnTrailingEdge;

        spiChConfig.sls.output.pin    = &SPI_3_CS_PIN;
        spiChConfig.sls.output.mode   = IfxPort_OutputMode_pushPull;
        spiChConfig.sls.output.driver = IfxPort_PadDriver_cmosAutomotiveSpeed1;

        spiChConfig.channelBasedCs= IfxQspi_SpiMaster_ChannelBasedCs_enabled;

        /* initialize channel */
        IfxQspi_SpiMaster_initChannel(&qspiCommunication->spiChannelQspi3tle9180, &spiChConfig);

        tleConfig.size = 8192;

        tleConfig.pins.enable   = TLE9180_ENABLE_PIN;
        tleConfig.pins.error    = TLE9180_ERROR_PIN;
        tleConfig.pins.inhibit  = TLE9180_INHIBIT_PIN;
        tleConfig.pins.safeOff  = TLE9180_SAFEOFF_PIN;

        tleConfig.spiChannel = &qspiCommunication->spiChannelQspi3tle9180;

        /*Configure the SPI interfaces as QSPI Master Interfaces*/
        tleConfig.spiIf.spiExchange= (IfxTLE9180_SpiExchangeIfPtrType)&IfxQspi_SpiMaster_exchange;

        IfxTLE9180_init(&qspiCommunication->driverTle9180, &tleConfig);
    }
}
